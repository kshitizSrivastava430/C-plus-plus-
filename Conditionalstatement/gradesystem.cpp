#include <iostream>
using namespace std;
int main () {
    int marks;
    cout<<"enter yours marks"<<endl;
    cin>>marks;
    if(marks>=90){
        cout<<"you got A grade"<<endl;
    }else if(marks>=80){
        cout<<"you got B grade"<<endl;
    }else if(marks>=70){
        cout<<"you got C grade"<<endl;
    }else if(marks>=60){
        cout<<"you got D grade"<<endl;
    }}